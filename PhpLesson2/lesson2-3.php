<?php  

function addition($x,$y){
    return $x + $y;
}

function subtraction($x,$y){
    return $x - $y;
}

function division($x,$y){
    return $x / $y;
}

function multiplication($x,$y){
    return $x * $y;
}


echo addition(1,2);
echo subtraction(1,2);
echo division(1,2);
echo multiplication(1,2);